import 'package:frs_mobile/models/product_detail_model.dart';

class ProductDetailBloc {
  static double getRentalPrice(
      int rentalPeriod, ProductDetailModel productDetailModel) {
    if (rentalPeriod == 4) {
      return productDetailModel.rentalPrice!.rentPrice4;
    } else if (rentalPeriod == 7) {
      return productDetailModel.rentalPrice!.rentPrice7;
    } else if (rentalPeriod == 10) {
      return productDetailModel.rentalPrice!.rentPrice10;
    }
    return 0.0;
  }
}
